"""Conjuntos: dinámica, no permite elementos repetidos. No es una estructura ordenada. 
Nos sirve para que podamos trabajar como en mate: intersección, unión, etc 
"""
conjunto0 = {1,2,3,4,5}
conjunto = set([1,2,3,4,5])
conjunto1 = set(["Te", "fuiste", "a", "la B"]) #Este cuando le mande print se va a ver desordenado

print(conjunto0)
print(conjunto)
print(conjunto1)